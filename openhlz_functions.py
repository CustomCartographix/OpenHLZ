def _writeAoiCutline(aoi_layer, scratch_folder):
    """
    Write the AOI polygon out to a shapefile so it can be used as a gdal.Warp
    cutline (gdal.Warp needs a data-source path, not an in-memory layer).
    """
    cutline_path = os.path.join(scratch_folder, 'aoi_cutline.shp')
    writeToShapefile(aoi_layer, cutline_path, only_selected=False)
    return cutline_path


def _makeWarpProgressCallback(feedback):
    """
    Build a GDAL progress callback that forwards progress to the Processing
    feedback panel and honours cancellation (returning 0 aborts the warp).
    """
    def _callback(complete, message, unknown):
        if feedback is not None:
            if feedback.isCanceled():
                return 0
            feedback.setProgress(complete * 100)
        return 1
    return _callback


def mosaicAndClipRasters(input_raster_filenames, aoi_layer, scratch_folder, output_raster, feedback=None):
    """
    Reproject, mosaic, and clip the input rasters to the AOI in a single
    streaming pass with osgeo.gdal.Warp.

    Warp reads block-by-block, so it never allocates the full output in RAM the
    way gdal:merge did (which blew up on large AOIs). It also natively accepts
    inputs in different UTM CRSs, applies the AOI cutline, preserves the source
    colour table, and writes an LZW-compressed, tiled GeoTIFF. Nearest-neighbour
    resampling is used so categorical land-cover class values are never blended
    into nonsense classes.
    """
    if not input_raster_filenames:
        raise QgsProcessingException('No input rasters were provided to mosaic and clip.')

    cutline_path = _writeAoiCutline(aoi_layer, scratch_folder)
    target_crs = aoi_layer.crs().authid()

    warp_options = gdal.WarpOptions(
        format='GTiff',
        dstSRS=target_crs,
        cutlineDSName=cutline_path,
        cropToCutline=True,
        resampleAlg='near',
        multithread=True,
        creationOptions=['COMPRESS=LZW', 'TILED=YES'],
        callback=_makeWarpProgressCallback(feedback))

    result = gdal.Warp(output_raster, input_raster_filenames, options=warp_options)
    if result is None:
        raise QgsProcessingException('GDAL Warp failed to mosaic and clip the input rasters.')

    # Flush and release the dataset / GDAL block cache
    result = None


