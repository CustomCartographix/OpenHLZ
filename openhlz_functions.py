def downloadCopernicusGlo30Data(aoi_layer, scratch_folder, project_coordinate_system_name, feedback=None):
    """
    Function to download Copernicus GLO-30 data based on input AOI using OpenTopography API.

    Returns output filename
    """
    # Get bounding box
    extent_var = aoi_layer.extent()
    extent_array = [extent_var.xMinimum(), extent_var.yMinimum(), extent_var.xMaximum(), extent_var.yMaximum()]
    input_projection = Proj(project_coordinate_system_name)
    output_projection = Proj('epsg:4326')
    min_x_proj, min_y_proj = transform(input_projection, output_projection, extent_array[0], extent_array[1])
    max_x_proj, max_y_proj = transform(input_projection, output_projection, extent_array[2], extent_array[3])
    url_bbox = ('south=' + str(min_x_proj) + '&north=' + str(max_x_proj) + '&west=' + str(min_y_proj) +
                '&east=' + str(max_y_proj))

    # Download Data
    download_url = ('https://portal.opentopography.org/API/globaldem?demtype=COP30&' + url_bbox +
                    '&outputFormat=GTiff&API_Key=8422c95f5e3490ecca69be478123007c')
    output_filename = os.path.join(scratch_folder, 'srtm_dem.tif')
    _blockingDownload(download_url, output_filename, 'Copernicus GLO-30 elevation data', feedback)

    # Return output filename
    return output_filename


def getJson(aoi_layer, scratch_folder, project_coordinate_system_name, feedback=None):
    """
    Function to get JSON file return for either DEM or LULC

    Returns JSON files for 1m, 10m, and 1/9 arc-second data
    """

    # Get bounding box
    extent_var = aoi_layer.extent()
    extent_array = [extent_var.xMinimum(), extent_var.yMinimum(), extent_var.xMaximum(), extent_var.yMaximum()]
    input_projection = Proj(project_coordinate_system_name)
    output_projection = Proj('epsg:4326')
    min_x_proj, min_y_proj = transform(input_projection, output_projection, extent_array[0], extent_array[1])
    max_x_proj, max_y_proj = transform(input_projection, output_projection, extent_array[2], extent_array[3])
    url_bbox = str(min_y_proj) + ',' + str(min_x_proj) + ',' + str(max_y_proj) + ',' + str(max_x_proj)

    # Get json return for 1m DEM
    url = 'https://tnmaccess.nationalmap.gov/api/v1/products?bbox=' + url_bbox + \
          '&datasets=Digital+Elevation+Model+(DEM)+1+meter&prodFormats=GeoTIFF&outputFormat=json'
    output_json_filename_1m = os.path.join(scratch_folder, 'json_1m.json')
    _blockingDownload(url, output_json_filename_1m, '1m DEM index', feedback)
    with open(output_json_filename_1m) as temp_file:
        json_1m = load(temp_file)

    # Get json return for 1/3 arc-second (10m) data
    url = 'https://tnmaccess.nationalmap.gov/api/v1/products?bbox=' + url_bbox + \
          ('&datasets=National%20Elevation%20Dataset%20('
           'NED)%201%20arc-second&prodFormats=GeoTIFF&outputFormat=json')
    output_json_filename_10m = os.path.join(scratch_folder, 'json_10m.json')
    _blockingDownload(url, output_json_filename_10m, '10m DEM index', feedback)
    with open(output_json_filename_10m) as temp_file_10m:
        json_10m = load(temp_file_10m)

    # Get json return for 1/9 arc-second (1m) DEM
    url = 'https://tnmaccess.nationalmap.gov/api/v1/products?bbox=' + url_bbox + \
          ('&datasets=National%20Elevation%20Dataset%20(NED)%201/9%20arc-second&prodFormats=GeoTIFF&'
           'outputFormat=json')
    output_json_filename_19as = os.path.join(scratch_folder, 'json_19as.json')
    _blockingDownload(url, output_json_filename_19as, '1/9 arc-second DEM index', feedback)
    with open(output_json_filename_19as) as temp_file:
        json_19as = load(temp_file)

    return json_1m, json_10m, json_19as


def downloadDem(i, json_file, scratch_folder, input_data, feedback=None):
    """
    Function to download DEM data based on input location. Will first try 1m, then will try 10m if no 1m

    Returns filename of saved DEM raster
    """

    # Download 1m data
    if input_data == '1m':
        temp_dict = json_file['items'][i]
        output_filename = os.path.join(scratch_folder, 'dem_1m_' + str(i) + '.tif')
        return _blockingDownload(temp_dict['downloadURL'], output_filename, '1m DEM tile', feedback)

    # Download 1/9m data
    if input_data == '19m':
        temp_dict = json_file['items'][i]
        output_filename = os.path.join(scratch_folder, 'dem_19m_' + str(i) + '.tif')
        return _blockingDownload(temp_dict['downloadURL'], output_filename, '1/9 arc-second DEM tile', feedback)

    # Else download 10m data
    else:
        temp_dict = json_file['items'][i]
        output_filename = os.path.join(scratch_folder, 'dem_10m_' + str(i) + '.tif')
        return _blockingDownload(temp_dict['downloadURL'], output_filename, '10m DEM tile', feedback)


def downloadLandCoverRaster(i, utm_zones, scratch_folder, feedback=None):
    """
    Function to download land cover data based on current location

    Returns filename of saved land cover raster
    """

    # Download data based on whichever UTM zone the aoi is within
    # Get UTM zone and create output filename
    zone = utm_zones[i]
    zone_id = str(zone[0]) + zone[1]
    output_filename = os.path.join(scratch_folder, zone_id + '.tif')

    # Download data from Living Atlas and write to scratch folder (2025 collection)
    url = ("https://lulctimeseries.blob.core.windows.net/lulctimeseriesv003/lc2025/" + zone_id +
           "_20250101-20251231.tif")
    _blockingDownload(url, output_filename, 'land cover tile ' + zone_id, feedback)

    # Return filename of downloaded land cover raster
    return output_filename


