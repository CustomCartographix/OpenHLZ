def _blockingDownload(url, output_filename, description, feedback=None):
    """
    Download the contents of a URL to a file using QgsNetworkAccessManager.

    Surfaces network failures as a QgsProcessingException (instead of silently
    writing an error page into the output file) and closes the file handle
    deterministically via a context manager.
    """
    request = QNetworkRequest(QUrl(url))
    network_access_manager = QgsNetworkAccessManager()
    response = network_access_manager.blockingGet(request)

    if response.error() != QNetworkReply.NoError:
        raise QgsProcessingException(
            'Network error while downloading ' + description + ': '
            + response.errorString() + ' (' + url + ')')

    content = response.content()
    with open(output_filename, 'wb') as output_file:
        output_file.write(content)

    if feedback is not None:
        feedback.pushInfo('Downloaded ' + description)

    return output_filename


def writeToShapefile(layer, output_path, only_selected=False):
    """
    Write a vector layer to an ESRI Shapefile using the non-deprecated
    writeAsVectorFormatV3 API.
    """
    options = QgsVectorFileWriter.SaveVectorOptions()
    options.driverName = 'ESRI Shapefile'
    options.fileEncoding = 'utf-8'
    options.onlySelectedFeatures = only_selected

    error = QgsVectorFileWriter.writeAsVectorFormatV3(
        layer,
        output_path,
        QgsProject.instance().transformContext(),
        options)

    if error[0] != QgsVectorFileWriter.NoError:
        raise QgsProcessingException(
            'Failed to write vector file ' + output_path + ': ' + str(error[1]))

    return output_path


