class _StyleFromQmlPostProcessor(QgsProcessingLayerPostProcessorInterface):
    """
    Base post-processor that applies a bundled .qml style to an output layer.

    The concrete subclass holds the created instance in a class attribute so it
    is not garbage-collected before QGIS calls back into it (the documented
    PyQGIS post-processor lifetime workaround).
    """
    instance = None
    style_path = None

    def postProcessLayer(self, layer, context, feedback):
        if self.style_path and layer is not None and layer.isValid():
            layer.loadNamedStyle(self.style_path)
            layer.triggerRepaint()

    @classmethod
    def create(cls, style_path):
        cls.instance = cls()
        cls.instance.style_path = style_path
        return cls.instance


class HlsRasterStylePostProcessor(_StyleFromQmlPostProcessor):
    instance = None
    style_path = None


class HlzPointsStylePostProcessor(_StyleFromQmlPostProcessor):
    instance = None
    style_path = None


def applyStyleOnLoad(context, output_layer_id, post_processor_cls, style_path):
    """
    Register a style post-processor for an output layer, if that layer will be
    loaded into the project on completion.
    """
    if context.willLoadLayerOnCompletion(output_layer_id):
        context.layerToLoadOnCompletionDetails(output_layer_id).setPostProcessor(
            post_processor_cls.create(style_path))
